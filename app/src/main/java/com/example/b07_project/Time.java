package com.example.b07_project;

public class Time {
    private int hour;
    private int min;


    public Time(){

    }

    public int getHour() {
        return hour;
    }
    public int getMin() {
        return min;
    }

    public void setHour(int hour) {
        this.hour = hour;
    }
    public void setMin(int min) {
        this.min = min;
    }
}
